#include "libft.h"

int	ft_has_slash(const char *str)
{
	int	i;


	if (str == NULL)
		return (0);
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '/')
			return (1);
		i++;
	}
	return (0);
}
